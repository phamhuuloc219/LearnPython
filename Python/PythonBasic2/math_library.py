# Hàm toán học cơ bản:

# acos(x): Tính giá trị arccos của x
# asin(x): Tính giá trị arcsin của x
# atan(x): Tính giá trị arctan của x
# atan2(y, x): Tính giá trị arctan2 của y và x
# ceil(x): Làm tròn số x lên số nguyên gần nhất
# cos(x): Tính giá trị cos của x (x là radian)
# cosh(x): Tính giá trị cosh của x
# exp(x): Tính giá trị e^x
# fabs(x): Tính giá trị tuyệt đối của x
# floor(x): Làm tròn số x xuống số nguyên gần nhất
# fmod(x, y): Tính giá trị dư của phép chia x cho y
# frexp(x): Phân tích số x thành dạng 2^m * f
# hypot(x, y): Tính căn bậc hai của x^2 + y^2
# ldexp(x, i): Nhân số x với 2^i
# log(x): Tính logarit cơ số e của x
# log10(x): Tính logarit cơ số 10 của x
# modf(x): Phân tích số x thành phần nguyên và phần thập phân
# pow(x, y): Tính giá trị x^y
# sin(x): Tính giá trị sin của x (x là radian)
# sinh(x): Tính giá trị sinh của x
# sqrt(x): Tính căn bậc hai của x
# tan(x): Tính giá trị tan của x (x là radian)
# tanh(x): Tính giá trị tanh của x

